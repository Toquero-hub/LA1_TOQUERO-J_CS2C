// 1. Create a variable named firstFavNumb and store your favorite number
let firstFavNumb = 25;

// 2. Create a variable named secondFavNumb and store your second favorite number
let secondFavNumb = 9; 

// 3. Perform the following checks:

// Greater than
let isGreaterThan = firstFavNumb > secondFavNumb;
console.log(firstFavNumb + " > " + secondFavNumb + " : " + isGreaterThan);

// Less than
let isLessThan = firstFavNumb < secondFavNumb;
console.log(firstFavNumb + " < " + secondFavNumb + " : " + isLessThan);

// Greater than or equal to
let isGreaterThanOrEqual = firstFavNumb >= secondFavNumb;
console.log(firstFavNumb + " >= " + secondFavNumb + " : " + isGreaterThanOrEqual);

// Less than or equal to
let isLessThanOrEqual = firstFavNumb <= secondFavNumb;
console.log(firstFavNumb + " <= " + secondFavNumb + " : " + isLessThanOrEqual);

// Strict equality (===)
let isStrictEqual = firstFavNumb === secondFavNumb;
console.log(firstFavNumb + " === " + secondFavNumb + " : " + isStrictEqual);

// Loose equality (==)
let isLooseEqual = firstFavNumb == secondFavNumb;
console.log(firstFavNumb + " == " + secondFavNumb + " : " + isLooseEqual);

// Strict inequality (!==)
let isStrictInequal = firstFavNumb !== secondFavNumb;
console.log(firstFavNumb + " !== " + secondFavNumb + " : " + isStrictInequal);

// Loose inequality (!=)
let isLooseInequal = firstFavNumb != secondFavNumb;
console.log(firstFavNumb + " != " + secondFavNumb + " : " + isLooseInequal);